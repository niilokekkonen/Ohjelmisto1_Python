print("Kerro seuraavaksi kolme kokonaislukua")
yksi = int(input("Ensimmäinen luku:"))
kaksi = int(input("Toinen luku:"))
kolme = int(input("Kolmas luku:"))

summa = yksi + kaksi + kolme
tulo = yksi * kaksi * kolme
keskiarvo = (yksi + kaksi + kolme) / 3
print(f"Tässä lukujen summa: {summa}")
print(f"Tässä lukujen tulo: {tulo}")
print(f"Tässä lukujen keskiarvo: {keskiarvo}")